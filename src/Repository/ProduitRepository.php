    /**
     * @return Produit[]
     */
    public function findAllgreaterThan($prix): array {
        $em = $this->getEntityManager();
        $query = $em->createQuery('SELECT p FROM App\Entity\Produit p 
        where p.prix > :prix
        ORDER BY p.prix ASC')->SetParameter('prix', $prix);
        return $query->getResult();
    }
